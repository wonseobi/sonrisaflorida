<!-- ── AILING VS FAILING DEFINITIONS ── -->
<section class="section">
  <div class="container">
    <div class="two-col">
      <div class="two-col-text">
        <span class="section-label">Understanding the Diagnosis</span>
        <h2>Ailing vs. Failing —<br><span class="text-primary">What's the Difference?</span></h2>
        <p><strong>An ailing implant</strong> is one that shows signs of disease or compromise — typically peri-implantitis (infection and bone loss around the implant) — but remains osseointegrated and clinically stable. It is fixable with prompt, appropriate intervention. The implant is in danger but not yet lost.</p>
        <p><strong>A failing implant</strong> has lost its osseointegration and exhibits clinical mobility, uncontrolled infection, or irreversible bone loss that cannot be treated conservatively. A failing implant will ultimately require removal — but even then, the site can be regenerated and a new implant placed once the area has healed.</p>
        <p>The critical distinction: early diagnosis of an ailing implant dramatically increases the odds of saving it. If you are experiencing any implant symptoms, act now — not after the next routine cleaning.</p>
        <ul class="experts-checklist">
          <li><i class="fa-solid fa-circle-check"></i> Ailing = osseointegrated but compromised — often saveable</li>
          <li><i class="fa-solid fa-circle-check"></i> Failing = lost osseointegration — requires intervention</li>
          <li><i class="fa-solid fa-circle-check"></i> Early diagnosis is the most important factor in outcome</li>
        </ul>
      </div>
