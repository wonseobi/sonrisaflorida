      <p style="color:var(--text-muted); margin-top:16px;">Don't dismiss these symptoms as normal. Any of the following warrants an urgent call to our office.</p>
    </div>
    <div class="tech-pillars">
      <div class="tech-pillar">
        <div class="tech-pillar-icon"><i class="fa-solid fa-arrows-up-down-left-right"></i></div>
        <h4>Implant Mobility</h4>
        <p>A properly integrated implant should be completely immobile. Any movement, even subtle, indicates loss of osseointegration and requires immediate evaluation.</p>
      </div>
      <div class="tech-pillar">
        <div class="tech-pillar-icon"><i class="fa-solid fa-bolt-lightning"></i></div>
        <h4>Pain on Loading</h4>
        <p>Pain or discomfort when biting or chewing — especially if it was not present before — is a red flag that the implant is under abnormal stress or the bone is compromised.</p>
      </div>
      <div class="tech-pillar">
        <div class="tech-pillar-icon"><i class="fa-solid fa-x-ray"></i></div>
        <h4>Bone Loss on X-Ray</h4>
        <p>Radiographic bone loss around the implant — particularly if progressive between films — is the hallmark of peri-implantitis and requires active treatment.</p>
      </div>
      <div class="tech-pillar">
        <div class="tech-pillar-icon"><i class="fa-solid fa-droplet"></i></div>
        <h4>Bleeding or Discharge</h4>
        <p>Persistent bleeding when the implant is probed, or any purulent discharge from the gum around the implant, indicates active infection.</p>
      </div>
      <div class="tech-pillar">
        <div class="tech-pillar-icon"><i class="fa-solid fa-eye"></i></div>
        <h4>Implant Exposure</h4>
        <p>Visible implant threads or metal through the gum indicates significant bone and tissue recession — the implant surface that should be buried is now exposed to the oral environment.</p>
      </div>
      <div class="tech-pillar">
        <div class="tech-pillar-icon"><i class="fa-solid fa-circle-xmark"></i></div>
        <h4>Crown Fracture</h4>
        <p>A broken crown on an implant may indicate occlusal overload or underlying abutment screw loosening — both of which can lead to implant damage if not addressed.</p>
      </div>
    </div>
  </div>
</section>

