<!-- ── TYPES OF GRAFTS ── -->
<section class="section section-light">
  <div class="container">
    <div style="text-align:center; max-width:600px; margin:0 auto 0;">
      <span class="section-label">Graft Types</span>
      <h2>Not All Bone Grafts<br><span class="text-primary">Are the Same.</span></h2>
      <p style="color:var(--text-muted); margin-top:16px;">The type of graft material used depends on the size of the defect, the location, and the patient's medical history. We discuss all options at your consultation.</p>
    </div>
    <div class="tech-pillars">
      <div class="tech-pillar">
        <div class="tech-pillar-icon"><i class="fa-solid fa-user"></i></div>
        <h4>Autograft</h4>
        <p>Bone taken from your own body (typically the chin or jaw) — the biological gold standard, but requires a second surgical site.</p>
      </div>
      <div class="tech-pillar">
        <div class="tech-pillar-icon"><i class="fa-solid fa-people-group"></i></div>
        <h4>Allograft</h4>
        <p>Donor bone from a human bone bank, processed and sterilized. The most common choice — eliminates a second surgical site.</p>
      </div>
      <div class="tech-pillar">
        <div class="tech-pillar-icon"><i class="fa-solid fa-paw"></i></div>
        <h4>Xenograft</h4>
        <p>Bovine (cow) or porcine (pig) bone mineral, processed to remove all organic material. Excellent scaffold for new bone formation.</p>
      </div>
      <div class="tech-pillar">
        <div class="tech-pillar-icon"><i class="fa-solid fa-flask"></i></div>
        <h4>Alloplast</h4>
        <p>Synthetic graft materials (hydroxyapatite, calcium phosphate) — biocompatible, no donor source required, consistent supply.</p>
      </div>
    </div>
  </div>
</section>

