        <span class="section-label">The Procedure</span>
        <h2>Rebuilding the Bone<br><span class="text-primary">That Was Lost.</span></h2>
        <p>When a tooth is lost and not replaced promptly, the jawbone beneath it begins to resorb — losing both height and width over time. For an implant to be placed, there must be adequate bone volume to fully encase the titanium post. When there isn't, a bone graft is performed first to rebuild that volume.</p>
        <p>A bone graft introduces graft material — derived from donor bone, synthetic sources, or occasionally your own bone — into the deficient area. Over a healing period of 3–6 months, your body replaces the graft material with new, living bone that can then support an implant. It is a well-established procedure with a high success rate.</p>
        <ul class="experts-checklist">
