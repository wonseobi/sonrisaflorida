      <div>
        <h3 style="font-size:1.1rem;margin-bottom:.5rem;">Do I need a referral from my dentist to see an orthodontist?</h3>
        <p style="color:#555;margin-bottom:2rem;">No. You can contact an orthodontist directly and schedule a consultation without a referral. Many patients self-refer after researching their options. However, your dentist is a great partner — they can often flag bite or alignment issues during your regular visits.</p>

        <h3 style="font-size:1.1rem;margin-bottom:.5rem;">Can my dentist do braces or Invisalign?</h3>
        <p style="color:#555;margin-bottom:2rem;">Some general dentists do offer basic Invisalign treatment. However, for complex cases — including significant crowding, bite issues, jaw discrepancies, or relapse from previous treatment — seeing a board-certified orthodontic specialist will typically produce a safer and more accurate result.</p>

        <h3 style="font-size:1.1rem;margin-bottom:.5rem;">Should I keep seeing my regular dentist while in orthodontic treatment?</h3>
        <p style="color:#555;">Absolutely. In fact, it's more important than ever. Braces and aligners can create new areas for plaque to accumulate, so maintaining your regular dental cleanings every 6 months is essential during treatment. Your dentist and orthodontist work as a team for your overall oral health.</p>
      </div>
      <div>
        <h3 style="font-size:1.1rem;margin-bottom:.5rem;">At what age should children first see an orthodontist?</h3>
        <p style="color:#555;margin-bottom:2rem;">The American Association of Orthodontists recommends a child's first orthodontic evaluation by age 7. This doesn't mean treatment will begin — most children don't start until 10–14 — but early evaluation allows us to monitor jaw development and intervene before issues become more complex and costly to treat.</p>

        <h3 style="font-size:1.1rem;margin-bottom:.5rem;">Is orthodontic treatment only for cosmetic purposes?</h3>
        <p style="color:#555;margin-bottom:2rem;">No. Orthodontic treatment addresses genuine health concerns. Misaligned bites can cause jaw pain, uneven tooth wear, difficulty chewing, speech issues, and increased risk of cavities in hard-to-clean areas. A straight smile is beautiful — but the functional benefits are equally important.</p>

        <h3 style="font-size:1.1rem;margin-bottom:.5rem;">How is an orthodontist different from a dentofacial orthopedist?</h3>
        <p style="color:#555;">They're the same specialty. The full title recognized by the ADA is "Orthodontics and Dentofacial Orthopedics." Orthodontists treat both tooth movement (orthodontics) and jaw relationship issues (dentofacial orthopedics) — the distinction is mainly regulatory.</p>
