      <div style="flex: 1;">
        <div style="margin-bottom: 2.5rem;">
          <h3 style="color: var(--color-primary); margin-bottom: .75rem; font-size: 1.2rem;">How long will my treatment take?</h3>
          <p>Treatment length varies significantly depending on the complexity of your case, the treatment type, and your compliance (especially with removable aligners). Simple cases may complete in as few as 6–9 months. Average braces or Invisalign treatment takes 12–24 months. Complex bite corrections involving multiple tooth movements or skeletal discrepancies can take up to 30 months. Your orthodontist will give you a projected timeline at your consultation — and we'll update that estimate as treatment progresses.</p>
        </div>
        <div style="margin-bottom: 2.5rem;">
          <h3 style="color: var(--color-primary); margin-bottom: .75rem; font-size: 1.2rem;">Does orthodontic treatment hurt?</h3>
          <p>Most patients experience some soreness after getting braces placed or receiving their first set of aligners, and again for 24–72 hours after each adjustment. This is normal and reflects the fact that your teeth are being moved — a physical process. The discomfort is generally mild and manageable with over-the-counter pain relievers. Most patients describe it as pressure rather than pain. Patients with Invisalign typically report less overall discomfort than those with braces.</p>
        </div>
        <div style="margin-bottom: 2.5rem;">
          <h3 style="color: var(--color-primary); margin-bottom: .75rem; font-size: 1.2rem;">How often do I need appointments?</h3>
          <p>For braces patients, adjustment appointments are typically every 4–8 weeks. For Invisalign patients, in-person visits are usually every 6–12 weeks — with virtual check-ins between. Lachalign patients may have only a few in-person visits over the entire course of treatment. Your specific schedule will depend on your treatment type and how your case is progressing.</p>
        </div>
        <div style="margin-bottom: 2.5rem;">
          <h3 style="color: var(--color-primary); margin-bottom: .75rem; font-size: 1.2rem;">Can I still play sports while in treatment?</h3>
          <p>Absolutely. Patients with braces should wear a mouthguard during contact sports — we can provide one, or you can purchase an over-the-counter model. Invisalign patients can remove their aligners for contact sports if preferred, then rinse and reinsert them afterward. Neither treatment prevents you from participating in any physical activity.</p>
        </div>
        <div style="margin-bottom: 2.5rem;">
          <h3 style="color: var(--color-primary); margin-bottom: .75rem; font-size: 1.2rem;">What can I eat with braces?</h3>
          <p>Most foods are fine with braces, but you'll need to avoid anything that could break brackets or bend wires. This means no hard candies, popcorn, raw carrots (cut them first), corn on the cob, bagels, and sticky foods like caramel or gummy candy. Soft foods — pasta, rice, eggs, soft fruits, cooked vegetables, dairy — are all fine. Most patients adapt quickly and find the dietary adjustments easy to manage.</p>
        </div>
        <div style="margin-bottom: 2.5rem;">
          <h3 style="color: var(--color-primary); margin-bottom: .75rem; font-size: 1.2rem;">How do I brush and floss with braces?</h3>
          <p>Brushing with braces requires a little more time and care than usual. Use a soft-bristled toothbrush at a 45-degree angle to clean along the gumline and around each bracket. We recommend brushing after every meal and before bed. For flossing, use a floss threader or orthodontic flosser to thread beneath the archwire between each tooth. Water flossers (like Waterpik) are also excellent for braces patients and can dramatically reduce the time needed for interdental cleaning.</p>
        </div>
      </div>
      <!-- Right Column -->
      <div style="flex: 1;">
        <div style="margin-bottom: 2.5rem;">
          <h3 style="color: var(--color-primary); margin-bottom: .75rem; font-size: 1.2rem;">What happens if a bracket comes loose?</h3>
          <p>A loose bracket is not an emergency, but it should be addressed within a few days. In the meantime, use orthodontic wax to cushion any sharp edge the loose bracket may create. Call our office and we'll schedule a quick repair appointment. Avoid pulling or twisting the loose bracket, and try to keep it in place with the wax until your visit. If the wire has also shifted and is causing significant discomfort, call us immediately for guidance.</p>
        </div>
        <div style="margin-bottom: 2.5rem;">
          <h3 style="color: var(--color-primary); margin-bottom: .75rem; font-size: 1.2rem;">Will I need to wear a retainer forever?</h3>
          <p>Yes — and that's actually good news, because it means your results will last forever too. Teeth are naturally inclined to shift back toward their original positions after orthodontic treatment, a process called relapse. Wearing your retainer as directed — typically full time for the first 3–6 months, then nightly — prevents relapse and preserves the smile you worked to achieve. Most patients eventually wear their retainer only a few nights per week. Skip the retainer long-term, and teeth will drift.</p>
        </div>
        <div style="margin-bottom: 2.5rem;">
          <h3 style="color: var(--color-primary); margin-bottom: .75rem; font-size: 1.2rem;">Can adults get orthodontic treatment?</h3>
          <p>Absolutely — and adult orthodontics is one of the fastest-growing segments of our practice. There is no age limit for orthodontic treatment. As long as your teeth and gums are healthy, your teeth can be moved. The mechanics are the same as for teenagers; the process may take slightly longer in some cases because adult bone is denser. We see adult patients from their 20s through their 60s and beyond. It's never too late for a smile you love.</p>
        </div>
        <div style="margin-bottom: 2.5rem;">
          <h3 style="color: var(--color-primary); margin-bottom: .75rem; font-size: 1.2rem;">What is the best age for my child to start?</h3>
          <p>The American Association of Orthodontists recommends that children receive their first orthodontic evaluation by age 7. At this age, we can identify developing issues early — before they become harder to treat. Phase I treatment (early intervention with expanders or other appliances) typically begins between ages 8 and 10 when indicated. Phase II treatment with braces or aligners usually begins between ages 11 and 14, once most permanent teeth have erupted. Not every child needs early treatment — but those who do benefit enormously from acting at the right time.</p>
        </div>
        <div style="margin-bottom: 2.5rem;">
          <h3 style="color: var(--color-primary); margin-bottom: .75rem; font-size: 1.2rem;">Is Invisalign as effective as braces?</h3>
          <p>For mild to moderate cases, yes — Invisalign achieves equivalent clinical results to braces, and in some categories (like minor spacing or mild crowding) it can be even more efficient. For complex cases involving significant vertical movement, severe rotations, or skeletal corrections, braces remain the more reliable and powerful option. The key variable is case selection: the right treatment matched to the right case always produces the best outcome. Your orthodontist will tell you honestly which option is better suited to your specific needs.</p>
        </div>
        <div style="margin-bottom: 2.5rem;">
          <h3 style="color: var(--color-primary); margin-bottom: .75rem; font-size: 1.2rem;">How do I know which treatment is right for me?</h3>
          <p>That's exactly what your free consultation is for. Our orthodontists evaluate your bite, review your X-rays and 3D scan, discuss your goals and lifestyle, and then present you with the options that are clinically appropriate for your case — along with honest cost and timeline information for each. You'll never be pushed toward a particular treatment. We believe informed patients make the best decisions, and our job is to give you everything you need to make yours confidently.</p>
