        <p>At every adjustment appointment, which typically occurs every 4–8 weeks. Your elastic ties are removed and replaced as part of every visit, so you get a fresh start with your color choice each time. You can stick with the same color you love or try something completely new — the choice is yours.</p>
      </div>
      <div style="margin-bottom: 2rem;">
        <h3 style="color: var(--primary); margin-bottom: .5rem;">Which colors look best on teeth?</h3>
        <p>It depends on personal preference and skin tone, but a few tips tend to hold true. Darker shades — navy, deep purple, forest green, royal blue — tend to make teeth look whiter by contrast. White and yellow elastics can pick up stains from food and beverages and may make teeth appear more yellow. Clear elastics look subtle but also stain easily. When in doubt, pick a color that makes you happy — you'll be wearing it for a few weeks!</p>
