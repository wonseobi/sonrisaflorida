      </div>
      <div class="step-item">
        <div class="step-number">03</div>
        <h4>Activation Period (2–4 Months)</h4>
        <p>Using a small key, parents turn the expander one increment per day as directed. This gradual pressure slowly separates the two halves of the palate, encouraging new bone to fill the gap. A small gap between the front teeth is normal and temporary during this phase.</p>
      </div>
      <div class="step-item">
        <div class="step-number">04</div>
        <h4>Retention Phase (4–6 Months)</h4>
        <p>Once the target width is achieved, the expander stays in place — but is no longer turned — for several months to allow the new bone to fully consolidate and harden. This prevents relapse and ensures lasting results.</p>
